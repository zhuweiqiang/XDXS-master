package com.qylm.dxo;

import com.qylm.dto.depot.ProductDepotAllotViewDto;
import com.qylm.entity.ProductDepotAllot;

public final class ProductDepotAllotViewDxo {

	public static void dtoToEntity(ProductDepotAllotViewDto dto, ProductDepotAllot entity) {
		entity.setDepotInfo(dto.getDepotInfo());
		entity.setProductStockDetail(dto.getProductStockDetail());
		entity.setAllotDepotInfo(dto.getAllotDepotInfo());
		entity.setAllotProductStockDetail(dto.getAllotProductStockDetail());
		entity.setNumber(dto.getNumber());
		entity.setState(dto.isState());
		entity.setCreater(dto.getCreater());
	}

	public static void entityToDto(ProductDepotAllot entity, ProductDepotAllotViewDto dto) {
		dto.setDepotInfo(entity.getDepotInfo());
		dto.setProductStockDetail(entity.getProductStockDetail());
		dto.setAllotDepotInfo(entity.getAllotDepotInfo());
		dto.setAllotProductStockDetail(entity.getAllotProductStockDetail());
		dto.setNumber(entity.getNumber());
		dto.setState(entity.isState());
		dto.setCreater(entity.getCreater());
	}

}
