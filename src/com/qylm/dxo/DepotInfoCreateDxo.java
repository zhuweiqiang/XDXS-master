package com.qylm.dxo;

import com.qylm.dto.depot.DepotInfoCreateDto;
import com.qylm.entity.DepotInfo;

public final class DepotInfoCreateDxo {

	public static void dtoToEntity(DepotInfoCreateDto dto, DepotInfo entity) {
		entity.setName(dto.getName());
		entity.setRemark(dto.getRemark());
		entity.setCreater(dto.getCreater());
		entity.setBelongingUser(dto.getBelongingUser());
	}

	public static void entityToDto(DepotInfo entity, DepotInfoCreateDto dto) {
		dto.setName(entity.getName());
		dto.setRemark(entity.getRemark());
		dto.setCreater(entity.getCreater());
		dto.setBelongingUser(entity.getBelongingUser());
	}

}
