package com.qylm.dto.baseSet;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.List;

import com.qylm.bean.returner.Returner;
import com.qylm.entity.Leaguer;
import com.qylm.entity.LeaguerDetail;
import com.qylm.entity.User;

/**
 * 保存卡项管理画面需要的值
 * @author smj
 */
public class LeaguerCreateDto implements Serializable {
	
	/**
	 * serialVersionUID
	 */
	private static final long serialVersionUID = 5679814404570946232L;

	/**
	 * 卡项级别
	 */
	private String level;
	
	/**
	 * 卡项价格
	 */
	private BigDecimal money;
	
	/**
	 * 卡项折扣（百分比）
	 */
	private BigDecimal rebate;
	
	/**
	 * 卡项详细
	 */
	private List<LeaguerDetail> leaguerDetailList;
	
	/**
	 * 对应的打折项目
	 */
	private List<LeaguerDetail> marketingProjectList;
	
	/**
	 * 对应的打折产品
	 */
	private List<LeaguerDetail> productStockList;
	
	/**
	 * 对应的赠送项目
	 */
	private List<LeaguerDetail> projectGiveList;
	
	/**
	 * 对应的赠送的现金卷
	 */
	private List<LeaguerDetail> gashRollList;
	
	/**
	 * 对应的赠送的身体卷
	 */
	private List<LeaguerDetail> bodyVolumeList;
	
	/**
	 * 创建事件
	 */
	private User creater;
	
	/**
	 * 上级
	 */
	private User belongingUser;
	
	/**
	 * 修改传值
	 */
	private Leaguer transferLeaguer;
	
	/**
	 * 返回共通方法
	 */
	private Returner<?, ?, ?> returner;

	/**
	 * @return the level
	 */
	public String getLevel() {
		return level;
	}

	/**
	 * @param level the level to set
	 */
	public void setLevel(String level) {
		this.level = level;
	}

	/**
	 * @return the money
	 */
	public BigDecimal getMoney() {
		return money;
	}

	/**
	 * @param money the money to set
	 */
	public void setMoney(BigDecimal money) {
		this.money = money;
	}

	/**
	 * @return the rebate
	 */
	public BigDecimal getRebate() {
		return rebate;
	}

	/**
	 * @param rebate the rebate to set
	 */
	public void setRebate(BigDecimal rebate) {
		this.rebate = rebate;
	}

	/**
	 * @return the leaguerDetailList
	 */
	public List<LeaguerDetail> getLeaguerDetailList() {
		return leaguerDetailList;
	}

	/**
	 * @param leaguerDetailList the leaguerDetailList to set
	 */
	public void setLeaguerDetailList(List<LeaguerDetail> leaguerDetailList) {
		this.leaguerDetailList = leaguerDetailList;
	}

	/**
	 * @return the marketingProjectList
	 */
	public List<LeaguerDetail> getMarketingProjectList() {
		return marketingProjectList;
	}

	/**
	 * @param marketingProjectList the marketingProjectList to set
	 */
	public void setMarketingProjectList(List<LeaguerDetail> marketingProjectList) {
		this.marketingProjectList = marketingProjectList;
	}

	/**
	 * @return the productStockList
	 */
	public List<LeaguerDetail> getProductStockList() {
		return productStockList;
	}

	/**
	 * @param productStockList the productStockList to set
	 */
	public void setProductStockList(List<LeaguerDetail> productStockList) {
		this.productStockList = productStockList;
	}

	/**
	 * @return the projectGiveList
	 */
	public List<LeaguerDetail> getProjectGiveList() {
		return projectGiveList;
	}

	/**
	 * @param projectGiveList the projectGiveList to set
	 */
	public void setProjectGiveList(List<LeaguerDetail> projectGiveList) {
		this.projectGiveList = projectGiveList;
	}

	/**
	 * @return the gashRollList
	 */
	public List<LeaguerDetail> getGashRollList() {
		return gashRollList;
	}

	/**
	 * @param gashRollList the gashRollList to set
	 */
	public void setGashRollList(List<LeaguerDetail> gashRollList) {
		this.gashRollList = gashRollList;
	}

	/**
	 * @return the bodyVolumeList
	 */
	public List<LeaguerDetail> getBodyVolumeList() {
		return bodyVolumeList;
	}

	/**
	 * @param bodyVolumeList the bodyVolumeList to set
	 */
	public void setBodyVolumeList(List<LeaguerDetail> bodyVolumeList) {
		this.bodyVolumeList = bodyVolumeList;
	}

	/**
	 * @return the creater
	 */
	public User getCreater() {
		return creater;
	}

	/**
	 * @param creater the creater to set
	 */
	public void setCreater(User creater) {
		this.creater = creater;
	}

	/**
	 * @return the belongingUser
	 */
	public User getBelongingUser() {
		return belongingUser;
	}

	/**
	 * @param belongingUser the belongingUser to set
	 */
	public void setBelongingUser(User belongingUser) {
		this.belongingUser = belongingUser;
	}

	/**
	 * @return the transferLeaguer
	 */
	public Leaguer getTransferLeaguer() {
		return transferLeaguer;
	}

	/**
	 * @param transferLeaguer the transferLeaguer to set
	 */
	public void setTransferLeaguer(Leaguer transferLeaguer) {
		this.transferLeaguer = transferLeaguer;
	}

	/**
	 * @return the returner
	 */
	public Returner<?, ?, ?> getReturner() {
		return returner;
	}

	/**
	 * @param returner the returner to set
	 */
	public void setReturner(Returner<?, ?, ?> returner) {
		this.returner = returner;
	}

}
