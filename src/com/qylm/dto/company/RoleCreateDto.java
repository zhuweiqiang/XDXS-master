package com.qylm.dto.company;

import java.io.Serializable;
import java.util.List;

import com.qylm.bean.returner.Returner;
import com.qylm.entity.Role;
import com.qylm.entity.RoleDetail;
import com.qylm.entity.User;

/**
 * 保存角色信息管理画面需要的值
 * @author smj
 */
public class RoleCreateDto implements Serializable {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 3757797805708777742L;
	
	/**
	 * 权限,角色
	 */
	private String roleName;

	/**
	 * 描述
	 */
	private String description;
	
	/**
	 * 选中值
	 */
	private String check;
	
	/**
	 * 角色详细
	 */
	private List<RoleDetail> roleDetailList;

	/**
	 * 创建角色
	 */
	private User creater;
	
	/**
	 * 上级
	 */
	private User belongingUser;
	
	/**
	 * 修改传值
	 */
	private Role transferRole;
	
	/**
	 * 返回共通方法
	 */
	private Returner<?, ?, ?> returner;

	/**
	 * @return the roleName
	 */
	public String getRoleName() {
		return roleName;
	}

	/**
	 * @param roleName the roleName to set
	 */
	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}

	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}

	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}

	/**
	 * @return the check
	 */
	public String getCheck() {
		return check;
	}

	/**
	 * @param check the check to set
	 */
	public void setCheck(String check) {
		this.check = check;
	}

	/**
	 * @return the roleDetailList
	 */
	public List<RoleDetail> getRoleDetailList() {
		return roleDetailList;
	}

	/**
	 * @param roleDetailList the roleDetailList to set
	 */
	public void setRoleDetailList(List<RoleDetail> roleDetailList) {
		this.roleDetailList = roleDetailList;
	}

	/**
	 * @return the creater
	 */
	public User getCreater() {
		return creater;
	}

	/**
	 * @param creater the creater to set
	 */
	public void setCreater(User creater) {
		this.creater = creater;
	}

	/**
	 * @return the belongingUser
	 */
	public User getBelongingUser() {
		return belongingUser;
	}

	/**
	 * @param belongingUser the belongingUser to set
	 */
	public void setBelongingUser(User belongingUser) {
		this.belongingUser = belongingUser;
	}

	/**
	 * @return the transferRole
	 */
	public Role getTransferRole() {
		return transferRole;
	}

	/**
	 * @param transferRole the transferRole to set
	 */
	public void setTransferRole(Role transferRole) {
		this.transferRole = transferRole;
	}

	/**
	 * @return the returner
	 */
	public Returner<?, ?, ?> getReturner() {
		return returner;
	}

	/**
	 * @param returner the returner to set
	 */
	public void setReturner(Returner<?, ?, ?> returner) {
		this.returner = returner;
	}

}
